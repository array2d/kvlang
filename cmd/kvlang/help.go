package main

import (
	"fmt"
	"os"
)

func showHelp() {
	fmt.Fprintln(os.Stderr, "kvlang — KV language VM interpreter")
	fmt.Fprintln(os.Stderr)
	fmt.Fprintln(os.Stderr, "usage:")
	fmt.Fprintln(os.Stderr, "  kvlang [<file.kv|dir>]         加载并执行（文件/目录）")
	fmt.Fprintln(os.Stderr, "  kvlang -c \"code\"               加载并执行内联代码")
	fmt.Fprintln(os.Stderr, "  echo \"code\" | kvlang            加载并执行管道代码")
	fmt.Fprintln(os.Stderr, "  kvlang serve                   启动 VM daemon")
	fmt.Fprintln(os.Stderr, "  kvlang load <file.kv|dir>      只加载到 kvspace，不执行")
	fmt.Fprintln(os.Stderr, "  kvlang vet <file.kv>           语法检查")
	fmt.Fprintln(os.Stderr, "  kvlang format <file.kv>        格式化源文件")
	fmt.Fprintln(os.Stderr, "  kvlang kvspace <cmd> [args]    KV 空间操作")
	fmt.Fprintln(os.Stderr, "  kvlang help                    显示此帮助")
	fmt.Fprintln(os.Stderr)
	fmt.Fprintln(os.Stderr, "kvspace 子命令:")
	fmt.Fprintln(os.Stderr, "  get <key>                      读取单个 key")
	fmt.Fprintln(os.Stderr, "  mget <key1> <key2> ...         批量读取")
	fmt.Fprintln(os.Stderr, "  set <key> <value>              写入 key")
	fmt.Fprintln(os.Stderr, "  del <key1> [key2 ...]          删除 key")
	fmt.Fprintln(os.Stderr, "  list <prefix>                  列出直接子项")
	fmt.Fprintln(os.Stderr, "  tree <prefix>                  递归树形结构")
	fmt.Fprintln(os.Stderr, "  dump <prefix>                  递归 key=value")
	fmt.Fprintln(os.Stderr, "  watch <key> [--timeout 5s]     阻塞等待通知")
	fmt.Fprintln(os.Stderr, "  notify <key> <value>           推送通知")
	fmt.Fprintln(os.Stderr, "  clear                          清空所有 kvspace 数据")
	fmt.Fprintln(os.Stderr, "  trace <vtid>                   追踪 vthread 单步执行，输出 NDJSON")
	fmt.Fprintln(os.Stderr)
	fmt.Fprintln(os.Stderr, "全局选项:")
	fmt.Fprintln(os.Stderr, "  --addr host:port               Redis 地址（默认 127.0.0.1:6379）")
	fmt.Fprintln(os.Stderr)
	fmt.Fprintln(os.Stderr, "示例:")
	fmt.Fprintln(os.Stderr, "  kvlang file.kv                 执行文件")
	fmt.Fprintln(os.Stderr, "  kvlang -c 'print(\"hi\")'        内联执行")
	fmt.Fprintln(os.Stderr, "  echo 'print(\"hi\")' | kvlang    管道执行")
	fmt.Fprintln(os.Stderr, "  kvlang load file.kv            加载不执行")
	fmt.Fprintln(os.Stderr, "  kvlang serve                   启动 daemon")
	fmt.Fprintln(os.Stderr, "  kvlang kvspace dump /src        查看已加载函数源码")
	os.Exit(0)
}
